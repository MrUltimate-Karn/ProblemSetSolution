ll bin_pow(ll x, ll p, ll mod) {
	if (p == 0) return 1;
	if (p & 1) return mult(bin_pow(x, p - 1, mod), x, mod);
	return bin_pow(mult(x, x, mod), p / 2, mod);
}

void solve()
{
	ll n;
	cin >> n;
	ll sum = 0, x = 9;
	vll ans;
	while (sum < n && x > 0) {
		ans.push_back(min(n - sum, x));
		sum += x;
		x--;
	}
	if (sum < n) {
		cout << -1 ;
	} else {
		sort(ans.begin(), ans.end());
		for (ll i : ans) {
			cout << i;
		}
	}
}



